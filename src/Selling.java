    double TotalPrize,grandtotal;
    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
       if(Prod_Name.getText().isEmpty()  || Prod_Quan.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Missing Information");
       }   
       else if(AvailableQty<Integer.parseInt(Prod_Quan.getText())){
           JOptionPane.showMessageDialog(null,"Not Available as much Quantity");
       }
       else{
          TotalPrize =Uprize *Double.parseDouble(Prod_Quan.getText());
          grandtotal+=TotalPrize;
        i++;
           if(i==1){
               BillText.setText(BillText.getText()+"\t Welcome To FamilyPoint\n"+"No   Name     Prize     Quantity     Total\n"+i+"     "+Prod_Name.getText() +"                "+Uprize+"             "+Prod_Quan.getText()+"              "+TotalPrize+"\n");
               
               
           }
           else{
               BillText.setText(BillText.getText()+i+"     "+Prod_Name.getText() +"                "+Uprize+"             "+Prod_Quan.getText()+"              "+TotalPrize+"\n");
           }
           TotalBill.setText("RS."+grandtotal);
           NetQuality = AvailableQty-Integer.parseInt(Prod_Quan.getText());
           update();
       }
    }//GEN-LAST:event_addbtnActionPerformed

