     public void Getcat(){
       try{
          Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/familypoint","root","1076");
          Statement st =con.createStatement();
          ResultSet rs= st.executeQuery("select * from familypoint.categorieslist");
          while(rs.next()){
              String mycat = rs.getString("Cate_name");
              Prod_Category.addItem(mycat);
          }
          
          ProductList.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
        } 
    }
    public void update(){
         try{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/familypoint","root","1076");
                String sql ="update familypoint.manageproducts set Prod_Quan=? where Prod_Name=?";
                PreparedStatement pstmt = con.prepareStatement(sql);
                pstmt.setInt(1,NetQuality);
               
                
                
                pstmt.setString(2, Prod_Name.getText());
                
                pstmt.executeUpdate();
                
                con.close();
                selectSeller();
                
            }
            catch( Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
    }
        
